export function getSkillsForClass(classId, level) {
  const result = []
  for (const skill of Object.values(SKILLS)) {
    if (skill.class === classId && (!skill.unlockLevel || level >= skill.unlockLevel)) {
      result.push(skill)
    }
  }
  return result
}
